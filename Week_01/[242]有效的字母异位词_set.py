import collections
# leetcode submit region begin(Prohibit modification and deletion)
class Solution(object):
    def isAnagram(self, s, t):
        """
        :type s: str
        :type t: str
        :rtype: bool
        """
        result = True
        sesm = set(s)
        if sesm == set(t):
            for i in sesm:
                result = result and (s.count(i) == t.count(i))
        else:
            return False
        return result
